
/*Creando la Funcion suma Estudiante1*/
public int Suma(int param1, int param2)
{
int resultado;
resultado=param1+param2;
return resultado;
}

/*Creando la Funcion Multiplicacion Estudiante1*/
public int Multiplicacion(int param1, int param2)
{
int resultado;
resultado=param1*param2;
return resultado;
}


/*Creando la Funcion Potencia Estudiante1*/
public int Potencia(int base, int exponente)
{
int resultado;
resultado=Math.pow(base,exponente);
return resultado;
}
